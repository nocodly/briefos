/** Wrap a body fragment into a full printable HTML document. */
export function renderDocument(title: string, bodyHtml: string): string {
  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8" />
<title>${escapeHtml(title)}</title>
<style>${BASE_CSS}</style>
</head>
<body>
<div class="page">
${bodyHtml}
<div class="footer">
  <span>Generated by BriefOS</span>
  <span class="mono">${escapeHtml(new Date().toISOString().slice(0, 10))}</span>
</div>
</div>
</body>
</html>`
}

// --- Small markup helpers shared by the exporters ---------------------------

export function bulletList(items: unknown[], emptyText = 'None'): string {
  if (!items || items.length === 0) return `<p class="muted">${escapeHtml(emptyText)}</p>`
  return `<ul class="clean">${items.map((i) => `<li>${escapeHtml(i)}</li>`).join('')}</ul>`
}

export function priorityPill(priority: string): string {
  const p = ['high', 'medium', 'low'].includes(priority) ? priority : 'low'
  return `<span class="pill ${p}">${escapeHtml(p)}</span>`
}

export function keywordCloud(keywords: unknown[]): string {
  if (!keywords || keywords.length === 0) return '<p class="muted">None</p>'
  return `<div class="keywords">${keywords
    .map((k) => `<span class="kw">${escapeHtml(k)}</span>`)
    .join('')}</div>`
}
